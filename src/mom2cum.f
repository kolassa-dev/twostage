      subroutine mom2cum(mm1,mm2,mm3,mm4,kk1,kk2,kk3,kk4)
      double precision mm1(2),mm2(2,2),mm3(2,2,2),mm4(2,2,2,2)
      double precision kk1(2),kk2(2,2),kk3(2,2,2),kk4(2,2,2,2)
      kk1(1)=
     -  mm1(1)
      kk1(2)=
     -  mm1(2)
      kk2(1,1)=
     -  -mm1(1)**2 + mm2(1,1)
      kk2(1,2)=
     -  -(mm1(1)*mm1(2)) + mm2(1,2)
      kk2(2,1)=kk2(1,2)
      kk2(2,2)=
     -  -mm1(2)**2 + mm2(2,2)
      kk3(1,1,1)=
     -  2*mm1(1)**3 - 3*mm1(1)*mm2(1,1) + mm3(1,1,1)
      kk3(1,1,2)=
     -          2*mm1(1)**2*mm1(2) - mm1(2)*mm2(1,1) - 
     -  2*mm1(1)*mm2(1,2) + mm3(1,1,2)
      kk3(1,2,1)=kk3(1,1,2)
      kk3(1,2,2)=
     -          2*mm1(1)*mm1(2)**2 - 2*mm1(2)*mm2(1,2) - 
     -  mm1(1)*mm2(2,2) + mm3(1,2,2)
      kk3(2,1,1)=kk3(1,1,2)
      kk3(2,1,2)=kk3(1,2,2)
      kk3(2,2,1)=kk3(1,2,2)
      kk3(2,2,2)=
     -  2*mm1(2)**3 - 3*mm1(2)*mm2(2,2) + mm3(2,2,2)
      kk4(1,1,1,1)=
     -          -6*mm1(1)**4 + 12*mm1(1)**2*mm2(1,1) - 
     -  3*mm2(1,1)**2 - 4*mm1(1)*mm3(1,1,1) + mm4(1,1,1,1)
      kk4(1,1,1,2)=
     -          -6*mm1(1)**3*mm1(2) + 6*mm1(1)*mm1(2)*mm2(1,1) + 
     -  6*mm1(1)**2*mm2(1,2) - 3*mm2(1,1)*mm2(1,2) - 
     -  mm1(2)*mm3(1,1,1) - 3*mm1(1)*mm3(1,1,2) + mm4(1,1,1,2)
      kk4(1,1,2,1)=kk4(1,1,1,2)
      kk4(1,1,2,2)=
     -          -6*mm1(1)**2*mm1(2)**2 + 2*mm1(2)**2*mm2(1,1) + 
     -  8*mm1(1)*mm1(2)*mm2(1,2) - 2*mm2(1,2)**2 + 
     -  2*mm1(1)**2*mm2(2,2) - mm2(1,1)*mm2(2,2) - 
     -  2*mm1(2)*mm3(1,1,2) - 2*mm1(1)*mm3(1,2,2) + 
     -  mm4(1,1,2,2)
      kk4(1,2,1,1)=kk4(1,1,1,2)
      kk4(1,2,1,2)=kk4(1,1,2,2)
      kk4(1,2,2,1)=kk4(1,1,2,2)
      kk4(1,2,2,2)=
     -          -6*mm1(1)*mm1(2)**3 + 6*mm1(2)**2*mm2(1,2) + 
     -  6*mm1(1)*mm1(2)*mm2(2,2) - 3*mm2(1,2)*mm2(2,2) - 
     -  3*mm1(2)*mm3(1,2,2) - mm1(1)*mm3(2,2,2) + mm4(1,2,2,2)
      kk4(2,1,1,1)=kk4(1,1,1,2)
      kk4(2,1,1,2)=kk4(1,1,2,2)
      kk4(2,1,2,1)=kk4(1,1,2,2)
      kk4(2,1,2,2)=kk4(1,2,2,2)
      kk4(2,2,1,1)=kk4(1,1,2,2)
      kk4(2,2,1,2)=kk4(1,2,2,2)
      kk4(2,2,2,1)=kk4(1,2,2,2)
      kk4(2,2,2,2)=
     -          -6*mm1(2)**4 + 12*mm1(2)**2*mm2(2,2) - 
     -  3*mm2(2,2)**2 - 4*mm1(2)*mm3(2,2,2) + mm4(2,2,2,2)
      return
      end
